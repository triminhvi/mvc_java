public class Student{
	private String name;
	private String rollNo;

	public String getName(){
		return this.name;
	}
	public void setName(String newName){
		this.name = newName;
	}
	public String getRollNo(){
		return this.rollNo;
	} 
	public String setRollNo(String newRollNo){
		this.rollNo = newRollno;
	}
}